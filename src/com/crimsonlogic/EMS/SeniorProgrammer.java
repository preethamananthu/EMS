package com.crimsonlogic.EMS;

public class SeniorProgrammer extends Programmer{
	private int noOfTrainees;
	private int noOfCertifications;
	private int shares;
	
	
	
	public int getShares() {
		return shares;
	}
	public void setShares(int shares) {
		this.shares = shares;
	}
	public int getNoOfTrainees() {
		return noOfTrainees;
	}
	public void setNoOfTrainees(int noOfTrainees) {
		this.noOfTrainees = noOfTrainees;
	}
	public int getNoOfCertifications() {
		return noOfCertifications;
	}
	public void setNoOfCertifications(int noOfCertifications) {
		this.noOfCertifications = noOfCertifications;
	}
	
	
}
